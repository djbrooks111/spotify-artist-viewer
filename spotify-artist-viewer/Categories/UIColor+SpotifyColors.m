#import "UIColor+SpotifyColors.h"

@implementation UIColor (SpotifyColors)

+ (UIColor *)aluminiumColor {
    return [UIColor colorWithRed:0.68
                           green:0.69
                            blue:0.71
                           alpha:1];
}

+ (UIColor *)oliveColor {
    return [UIColor colorWithRed:0.4
                           green:0.56
                            blue:0.09
                           alpha:1];
}

@end
